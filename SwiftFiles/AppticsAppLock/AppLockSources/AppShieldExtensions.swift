extension String{
    
    func getLocalizedString()->String{
        if let localizer =  AppticsLock.shared.customLocalized{
            switch self{
            case "app.haslocked":
                return localizer.yourAppHasLocked()
            case  "app.authenticate.authenticate":
                return localizer.appShieldNeedsAuthentication()
            case "app.authenticate.setup":
                return localizer.appShieldSetup()
            case "app.settings":
                return localizer.appShielSetting()
            default:
                return localizer.appShielSetting()
            }

        }
        else{
            return getStringFromBundle()
        }

    }
    internal func getStringFromBundle()->String{
        let path = Bundle(for: AuthenticatorViewController.self).path(forResource: "AppShield", ofType: "bundle")
        
        var bundle:Bundle?
        
        if let path{
             bundle = Bundle(path: path) ?? Bundle.main
        }
        else {
#if USING_SPM
             bundle =  Bundle.module
            #endif
        }
       
        return NSLocalizedString(self, bundle: bundle ?? .main, comment: "")

    }
    
    func getTimeStringForDeay()->String{

        return "controller.after".getLocalizedString() + " " + self + " " + "controller.minutes".getLocalizedString()

    }
}
extension UIImage {
    /// Shared CIContext — creating one is expensive (allocates GPU resources), so reuse a single
    /// instance instead of building a new one on every averageColor call.
    private static let sharedAverageColorContext = CIContext(options: [.workingColorSpace: kCFNull as Any])

    var averageColor: UIColor? {
        guard let inputImage = CIImage(image: self) else { return nil }
        let extentVector = CIVector(x: 0, y: 0, z: 1, w: 1)

        guard let filter = CIFilter(name: "CIAreaAverage", parameters: [kCIInputImageKey: inputImage, kCIInputExtentKey: extentVector]) else { return nil }
        guard let outputImage = filter.outputImage else { return nil }

        var bitmap = [UInt8](repeating: 0, count: 4)
        let context = UIImage.sharedAverageColorContext
        context.render(outputImage, toBitmap: &bitmap, rowBytes: 4, bounds: CGRect(x: 0, y: 0, width: 1, height: 1), format: .RGBA8, colorSpace: nil)
        
        return UIColor(red: CGFloat(bitmap[0]) / 255, green: CGFloat(bitmap[1]) / 255, blue: CGFloat(bitmap[2]) / 255, alpha: CGFloat(bitmap[3]) / 255)
    }
}
extension UIImageView {
  func setImageColor(color: UIColor) {
    let templateImage = self.image?.withRenderingMode(.alwaysTemplate)
    self.image = templateImage
    self.tintColor = color
  }
}


extension Bundle {
    public var icon: UIImage? {
        if let icons = infoDictionary?["CFBundleIcons"] as? [String: Any],
            let primaryIcon = icons["CFBundlePrimaryIcon"] as? [String: Any],
            let iconFiles = primaryIcon["CFBundleIconFiles"] as? [String],
            let lastIcon = iconFiles.last {
            return UIImage(named: lastIcon)
        }
        return nil
    }
}




